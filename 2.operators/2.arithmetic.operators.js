/**
 * @description Arithmetic Operators
 */

let x = 30;
let y = 3;

console.log(x + y); // Addition
console.log(x - y); // Subtraction
console.log(x * y); // Multiplication
console.log(x / y); // Division
console.log(x % y); // Remainder
console.log(x ** y); // Exponential - New

/**
 * Two new Arithmetic Operators
 * Tricky
 * Increment & Decrement
 */

// The value is incremented first and then displayed
console.log(++x);

// The value is displayed then incremented
console.log(x++);
console.log(x); // To see the incremented value

// The value is decremented first and then displayed
console.log(--x);

// The value is displayed then decremented
console.log(x--);
console.log(x); // To see the decremented value

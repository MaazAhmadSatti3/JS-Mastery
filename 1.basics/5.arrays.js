/**
 * @description Arrays
 * Remember always to use a meaningful name for variables
 * [] represents an array literal
 */

// An array
let selectedColors = ["red", "blue"];

// Added an element of same type to the array
selectedColors[2] = "green";

// Added an element of different type to the array
selectedColors[3] = 4;

//Type of an array
console.log(typeof selectedColors);

console.log(selectedColors);
